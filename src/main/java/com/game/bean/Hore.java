package com.game.bean;

public class Hore {

	private int id;
	private int horeId;
	private int lvl;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the horeId
	 */
	public int getHoreId() {
		return horeId;
	}
	/**
	 * @param horeId the horeId to set
	 */
	public void setHoreId(int horeId) {
		this.horeId = horeId;
	}
	/**
	 * @return the lvl
	 */
	public int getLvl() {
		return lvl;
	}
	/**
	 * @param lvl the lvl to set
	 */
	public void setLvl(int lvl) {
		this.lvl = lvl;
	}
	
	
}
