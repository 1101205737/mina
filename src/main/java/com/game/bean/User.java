package com.game.bean;

public class User {

	private int id;
	private int name;
	private int lvl;
	
	private int gload;
	
	private int tili;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public int getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(int name) {
		this.name = name;
	}

	/**
	 * @return the lvl
	 */
	public int getLvl() {
		return lvl;
	}

	/**
	 * @param lvl the lvl to set
	 */
	public void setLvl(int lvl) {
		this.lvl = lvl;
	}

	/**
	 * @return the gload
	 */
	public int getGload() {
		return gload;
	}

	/**
	 * @param gload the gload to set
	 */
	public void setGload(int gload) {
		this.gload = gload;
	}

	/**
	 * @return the tili
	 */
	public int getTili() {
		return tili;
	}

	/**
	 * @param tili the tili to set
	 */
	public void setTili(int tili) {
		this.tili = tili;
	}
	
	
}
